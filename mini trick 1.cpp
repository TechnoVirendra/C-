/*
Write a program that generates the following output :
		5
		10	
		9					
Assign value 5 to a variable using = Multiply it with 2 to generate 10 and subtract 1 to generate 9.  */
#include<iostream>
using namespace std;
int main()
{
	int n=5;
	cout <<"\t"<< n <<"\n\t"<<n*2 <<"\n\t" <<(n*2)-1;
	return 0;
}
