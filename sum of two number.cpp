// write a program to make a sum of two numbers .
#include<stdio.h>
int main()
{
	int a,b,c;								 // taking three Variable.    
	printf("Enter Two Number :");			// print Enter Two Number
	scanf("%d%d",&a,&b);					// input two number 
	c=a+b;									// sum formula.
	printf("Sum of Two Numbers =%d",c);		// print sum 
	return 0;
}
